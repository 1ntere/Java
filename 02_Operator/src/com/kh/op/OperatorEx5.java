package com.kh.op;

public class OperatorEx5 {

	public static void main(String[] args) {
		/*
		 비교 연산자 (==, !=)
		 	== : 서로 같으면 true
		 	ex) 5 == 5 : true
		     	3 == 5 : false
		     	5 == 3 : false
		 	!= : 서로 다르면 true
		 	ex) 5 != 5 : false
		     	3 != 5 : true
		     	5 != 3 : true
		 
		 논리 연산자 (&&, ||)
		 	&& : 왼쪽, 오른쪽 두 개의 조건이 모두 참일때만 전체 표현이 참
		 	ex) (5==5) && (3==3) : true
		       	 true      true
		     	(5!=3) && (3==3) : true
		      	 true      true
		     	(5==5) && (5==3) : false
		      	 true     false
		     	(3==5) && (5==5) : false
		     	false      true
		 	|| : 왼쪽, 오른쪽 둘 중 하나라도 참이면 참
		 	ex) (5==5) || (3==3) : true
		      	 true      true
		     	(5!=3) || (3==3) : true
		      	 true      true
		     	(5==5) || (5==3) : true
		      	 true     false
		     	(3==5) || (5==5) : true
		     	false      true
		     	(3==5) || (5!=5) : false
		     	false     false
		 
		 비교 연산자 (>, <, >=, <=)
		 왼쪽 > 오른쪽 : 오른쪽의 값보다 왼쪽이 크면 true
		 왼쪽 < 오른쪽 : 왼쪽의 값보다 오른쪽이 크면 true
		 왼쪽 >= 오른쪽 : 오른쪽의 값보다 왼쪽이 크거나 같으면 true
		 왼쪽 <= 오른쪽 : 왼쪽의 값보다 오른쪽이 크거나 같으면 true
		 */
		
		int num1 = 10;
		int num2 = 20;
		
		// num1 과 num2 값이 1. 같은지, 2. 다른지, 3. 어떤 값이 더 큰지
		
		boolean result1 = num1 == num2;
		//num1, num2 같다면 참
		boolean result2 = num1 != num2;
		//num1, num2 다르다면 참
		boolean result3 = num1 < num2;
		//num2가 num1보다 크다면 참
		
		System.out.println("num1 과 num2가 같은가? : "+result1);
		System.out.println("num1 과 num2가 다른가? : "+result2);
		System.out.println("num2 가 num1 보다 큰가? : "+result3);
	}

}
